@dataclass
class DataCollatorForSupervisedDataset(object):
    """Collate examples for supervised fine-tuning."""
    tokenizer: transformers.PreTrainedTokenizer
    
    def __call__(self, instances: Sequence[Dict]) -> Dict[str, torch.Tensor]:
        input_ids, labels = tuple([instance[key] for instance in instances]
                                  for key in ("input_ids", "labels"))
        input_ids = torch.nn.utils.rnn.pad_sequence(
            input_ids,
            batch_first=True,
            padding_value=self.tokenizer.pad_token_id)
        labels = torch.nn.utils.rnn.pad_sequence(labels,
                                                 batch_first=True,
                                                 padding_value=IGNORE_INDEX)
        input_ids = input_ids[:, :self.tokenizer.model_max_length]
        labels = labels[:, :self.tokenizer.model_max_length]
        batch = dict(
            input_ids=input_ids,
            labels=labels,
            attention_mask=input_ids.ne(self.tokenizer.pad_token_id),
        )
        if 'pet_image' in instances[0] and 'ct_image' in instances[0]:
            pet_images = [instance['pet_image'] for instance in instances]
            ct_images = [instance['ct_image'] for instance in instances]
            if all(x is not None and x.shape == pet_images[0].shape for x in pet_images) and \
                all(x is not None and x.shape == ct_images[0].shape for x in ct_images):
                batch['images'] = { 'PET': torch.stack(pet_images), 'CT': torch.stack(ct_images) } 
            else:
                batch['images'] = { 'PET': pet_images, 'CT': ct_images }
        else:
            images = [instance['image'] for instance in instances]
            if all(x is not None and x.shape == images[0].shape for x in images):
                batch['images'] = { 'data': torch.stack(images) }  
            else:
                batch['images'] = { 'data': images }
        return batch


# LIGHTNING DATA MODULE
class MultimodalDataModule(pl.LightningDataModule):
    """Lightning DataModule for multimodal supervised fine-tuning."""
    
    def __init__(
        self,
        train_data_path: str,
        val_data_path: Optional[str] = None,
        test_data_path: Optional[str] = None,
        data_type: str = "PET",
        tokenizer: transformers.PreTrainedTokenizer = None,
        data_args: Any = None,
        augment: callable = None,
        batch_size: int = 8,
        num_workers: int = 4,
        pin_memory: bool = True,
    ):
        super().__init__()
        self.save_hyperparameters(ignore=['tokenizer', 'data_args', 'augment'])
        
        self.train_data_path = train_data_path
        self.val_data_path = val_data_path
        self.test_data_path = test_data_path
        self.data_type = data_type
        self.tokenizer = tokenizer
        self.data_args = data_args
        self.augment = augment
        self.batch_size = batch_size
        self.num_workers = num_workers
        self.pin_memory = pin_memory
        
        self.collate_fn = DataCollatorForSupervisedDataset(tokenizer=self.tokenizer)
    
    def setup(self, stage: Optional[str] = None):
        if stage == "fit" or stage is None:
            self.train_dataset = LazySupervisedDataset(
                type=self.data_type,
                data_path=self.train_data_path,
                augment=self.augment,
                tokenizer=self.tokenizer,
                data_args=self.data_args
            )
            
            if self.val_data_path:
                self.val_dataset = LazySupervisedDataset(
                    type=self.data_type,
                    data_path=self.val_data_path,
                    augment=None,  # No augmentation for validation
                    tokenizer=self.tokenizer,
                    data_args=self.data_args
                )
        
        if stage == "test" or stage is None:
            if self.test_data_path:
                self.test_dataset = LazySupervisedDataset(
                    type=self.data_type,
                    data_path=self.test_data_path,
                    augment=None,  # No augmentation for test
                    tokenizer=self.tokenizer,
                    data_args=self.data_args
                )
    
    def train_dataloader(self):
        return DataLoader(
            self.train_dataset,
            batch_size=self.batch_size,
            shuffle=True,
            num_workers=self.num_workers,
            pin_memory=self.pin_memory,
            collate_fn=self.collate_fn,
            drop_last=True
        )
    
    def val_dataloader(self):
        if hasattr(self, 'val_dataset'):
            return DataLoader(
                self.val_dataset,
                batch_size=self.batch_size,
                shuffle=False,
                num_workers=self.num_workers,
                pin_memory=self.pin_memory,
                collate_fn=self.collate_fn
            )
        return None
    
    def test_dataloader(self):
        if hasattr(self, 'test_dataset'):
            return DataLoader(
                self.test_dataset,
                batch_size=self.batch_size,
                shuffle=False,
                num_workers=self.num_workers,
                pin_memory=self.pin_memory,
                collate_fn=self.collate_fn
            )
        return None


